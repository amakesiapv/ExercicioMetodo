package dio.exercicio;

public class Calculadora {
    public static void soma(double num1, double num2) {
        double resultado = num1 + num2;
        System.out.println("A soma dos dois numeros é: " + resultado);
    }

    public static void subtracao(double num1, double num2) {
        double resultado = num1 - num2;
        System.out.println("A subtração dos dois numeros é: " + resultado);
    }

    public static void multiplicacao(double num1, double num2) {
        double resultado = num1 + num2;
        System.out.println("A multiplicaco dos dois numeros é: " + resultado);
    }

    public static void divisao(double num1, double num2) {
        double resultado = num1 + num2;
        System.out.println("A divisao dos numeros é: " + resultado);
    }
}